# ---------- 5. Evaluation ----------
def asymmetric_loss(y_hat, y_true, alpha=2.0):
    # alpha > 1 → under-prediction punished more
    diff = y_hat - y_true
    under_mask = (diff < 0).float()      # y_hat < y_true
    over_mask  = (diff >= 0).float()     # y_hat >= y_true

    under_loss = (diff ** 2) * alpha * under_mask
    over_loss  = (diff ** 2) * over_mask

    return (under_loss + over_loss).mean()

