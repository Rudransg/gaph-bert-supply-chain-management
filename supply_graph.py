# ---------- 3. Heterogeneous GCN over the four relation types ----------
class HeteroGCN(nn.Module):
    def __init__(self, in_channels, hidden_channels, out_channels):
        super().__init__()
        # Correct the destination node type to 'rolled_prod' for consistency with input features
        self.conv1 = HeteroConv({
            ('rolled_prod', 'same_plant', 'rolled_prod'): SAGEConv(in_channels, hidden_channels),
            ('rolled_prod', 'same_storage', 'rolled_prod'): SAGEConv(in_channels, hidden_channels),
            ('rolled_prod', 'same_product_group', 'rolled_prod'): SAGEConv(in_channels, hidden_channels),
            ('rolled_prod', 'same_product_subgroup', 'rolled_prod'): SAGEConv(in_channels, hidden_channels),
        }, aggr='sum')

        self.conv2 = HeteroConv({
            ('rolled_prod', 'same_plant', 'rolled_prod'): SAGEConv(hidden_channels, hidden_channels),
            ('rolled_prod', 'same_storage', 'rolled_prod'): SAGEConv(hidden_channels, hidden_channels),
            ('rolled_prod', 'same_product_group', 'rolled_prod'): SAGEConv(hidden_channels, hidden_channels),
            ('rolled_prod', 'same_product_subgroup', 'rolled_prod'): SAGEConv(hidden_channels, hidden_channels),
        }, aggr='sum')

        self.lin = nn.Linear(hidden_channels, out_channels)

    def forward(self, x_dict, edge_index_dict):
        # Transform edge_index_dict keys to be consistent with 'rolled_prod' as destination node type
        transformed_edge_index_dict = {}
        for (src_type, rel_type, dst_type), edge_index_tensor in edge_index_dict.items():
            if dst_type == 'product':
                transformed_edge_index_dict[(src_type, rel_type, 'rolled_prod')] = edge_index_tensor
            else:
                transformed_edge_index_dict[(src_type, rel_type, dst_type)] = edge_index_tensor

        x_dict = self.conv1(x_dict, transformed_edge_index_dict)
        x_dict = {k: F.relu(v) for k, v in x_dict.items()}
        x_dict = self.conv2(x_dict, transformed_edge_index_dict)
        x_dict = {k: F.relu(v) for k, v in x_dict.items()}
        out = self.lin(x_dict['rolled_prod'])  # [N, out_channels]
        return out

